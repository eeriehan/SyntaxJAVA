package SYNTAX_JAVA.class12_STRING;

public class StringDemo02_ToUpperLower {
    public static void main(String[] args) {
        String demo2 = "dna";
        // If we need to convert something from lowercase to upper case we can use this method.
        System.out.println(demo2.toUpperCase());

        demo2 = "JAVA";
        System.out.println(demo2.toLowerCase());

    }
}
